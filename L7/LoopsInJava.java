public class LoopsInJava {
    public static void main(String a[]){
        // while, do while and for loops
        int count = 0;
        while(count <3){
            System.out.println(count);
            count++;
        }

        // do while
        do{
            count--;
            System.out.println(count);
        }while(count > 0);

        // for
        for(int i=0; i<3; i++){
            System.out.println(i);
        }

        // Continue statement
        // break statement
    }
}
